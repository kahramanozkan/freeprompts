// Get translation function
export function getTranslation(lang: string, key: string): string {
  const language = translations[lang as keyof typeof translations];
  if (!language) {
    // Fallback to English if language not found
    return (translations.english as any)[key] || key;
  }
  
  return (language as any)[key] || key;
}